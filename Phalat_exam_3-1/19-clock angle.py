x=input("เวลา: ")
h=x[:2]
m=x[3:]
h=int(h)
m=int(m)
am=360/60 #องศาที่ได้ของเข็ม 1 นาที
ah=360/12 #องศาที่ได้ของเข็ม 1 ชั่วโมง
agm=am*m
agh=ah*h
Nm=(m*30)/60 #เข็มสั่นเดิน60เข็มยาวเดิน30
if(agh>agm):
    C=agh-agm+Nm
    CC=360-C
    print("นับทวนเข็มห่างกัน",CC,"องศา")
    print("นับตามเข็มห่างกัน",C,"องศา")
else:
    C=agm-agh-Nm
    CC=360-C
    print("นับตามเข็มห่างกัน",C,"องศา")
    print("นับทวนเข็มห่างกัน",CC,"องศา")